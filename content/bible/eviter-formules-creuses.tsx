export default function GuideEviterFormulesCreuses() {
  return (
    <div className="prose-guide">
      <p>
        Certaines phrases sont si répandues dans les lettres de motivation qu&apos;elles ont perdu toute signification. Elles ne disent rien de vous, ne prouvent rien, et n&apos;ont aucun impact sur la décision du recruteur. Les supprimer et les remplacer par des formulations concrètes est l&apos;une des modifications les plus efficaces que vous puissiez apporter à votre lettre.
      </p>

      <h2>15 formules à bannir — et par quoi les remplacer</h2>
      <ul>
        <li>
          <strong>"Je suis très motivé par cette opportunité"</strong> → Remplacez par une preuve : "J&apos;ai testé votre produit pendant trois mois et voici ce que j&apos;en ai retenu."
        </li>
        <li>
          <strong>"Passionné par le secteur de…"</strong> → Remplacez par une action : "Je suis les travaux de [nom d&apos;un expert ou d&apos;une publication sectorielle] depuis deux ans."
        </li>
        <li>
          <strong>"Fort de X années d&apos;expérience"</strong> → Remplacez par un résultat : "En cinq ans à ce poste, j&apos;ai réduit les délais de livraison de 30 %."
        </li>
        <li>
          <strong>"Je suis dynamique, rigoureux et polyvalent"</strong> → Remplacez par une situation : "J&apos;ai géré simultanément trois projets clients en période de pic d&apos;activité, sans dépasser les délais."
        </li>
        <li>
          <strong>"Votre entreprise est reconnue pour son excellence"</strong> → Remplacez par un détail précis : "Votre approche sur [aspect spécifique] m&apos;a convaincu que c&apos;est l&apos;environnement dans lequel je veux travailler."
        </li>
        <li>
          <strong>"Je souhaite mettre mes compétences au service de votre entreprise"</strong> → Remplacez par une offre concrète : "Je peux structurer votre processus de onboarding — un problème que j&apos;ai résolu pour une équipe de 15 personnes."
        </li>
        <li>
          <strong>"Cette opportunité représente un défi stimulant"</strong> → Supprimez. Cette phrase ne dit rien que le recruteur ne sache pas déjà.
        </li>
        <li>
          <strong>"Dans l&apos;espoir que ma candidature retiendra votre attention"</strong> → Remplacez par : "Je serais heureux d&apos;en discuter lors d&apos;un entretien à votre convenance."
        </li>
        <li>
          <strong>"Je reste à votre disposition pour tout renseignement complémentaire"</strong> → Supprimez ou remplacez par votre numéro de téléphone dans la signature.
        </li>
        <li>
          <strong>"Persuadé de pouvoir apporter une réelle valeur ajoutée"</strong> → Dites quelle valeur, avec un exemple : "J&apos;ai triplé le taux de conversion email en six mois — voici comment."
        </li>
        <li>
          <strong>"Mon parcours m&apos;a permis de développer de nombreuses compétences"</strong> → Nommez-en deux, précisément, avec un contexte.
        </li>
        <li>
          <strong>"Je suis une personne de contact facile"</strong> → Décrivez une situation où cela a compté.
        </li>
        <li>
          <strong>"Je vous adresse ci-joint mon CV pour votre examen"</strong> → Supprimez. Le CV est joint, le recruteur le sait.
        </li>
        <li>
          <strong>"Je nourris un vif intérêt pour votre secteur d&apos;activité"</strong> → Montrez cet intérêt par une référence concrète à leur actualité.
        </li>
        <li>
          <strong>"Soucieux de m&apos;investir pleinement dans mes missions"</strong> → Donnez un exemple de cet investissement.
        </li>
      </ul>

      <h2>La règle à appliquer à chaque phrase</h2>
      <p>
        Avant de conserver une phrase dans votre lettre, posez-vous cette question : est-ce que cette phrase pourrait figurer dans la lettre de n&apos;importe quel autre candidat pour ce poste, sans changer un mot ? Si la réponse est oui, la phrase est creuse. Réécrivez-la avec un exemple précis ou supprimez-la.
      </p>

      <blockquote>
        Chaque phrase de votre lettre doit gagner sa place. Si elle ne dit rien de spécifique sur vous ou sur eux, elle affaiblit le reste du texte.
      </blockquote>
    </div>
  )
}
