export default function GuideLoisirsCVs() {
  return (
    <div className="prose-guide">
      <p>
        La section loisirs et centres d&apos;intérêt est la plus controversée du CV. Certains recruteurs l&apos;adorent — elle humanise le candidat. D&apos;autres l&apos;ignorent complètement. Dans les deux cas, cette section peut vous aider ou vous nuire selon ce que vous y mettez et comment vous le formulez.
      </p>

      <h2>Pourquoi inclure des loisirs : les arguments réels</h2>
      <p>
        Une section loisirs bien rédigée sert trois objectifs concrets :
      </p>
      <ul>
        <li><strong>Compenser un manque d&apos;expérience :</strong> pour les profils juniors, les activités extraprofessionnelles (sport de compétition, association, projet personnel) peuvent illustrer des qualités que les expériences professionnelles ne montrent pas encore</li>
        <li><strong>Créer une accroche en entretien :</strong> un recruteur cherche parfois un point d&apos;entrée pour briser la glace. Un loisir original ou un engagement associatif peut devenir ce déclencheur</li>
        <li><strong>Signaler des compétences transversales :</strong> "entraîneur d&apos;une équipe de basket junior" dit quelque chose sur vos capacités à fédérer et à transmettre</li>
      </ul>

      <h2>Ce qui fonctionne dans cette section</h2>
      <p>
        Les loisirs qui apportent de la valeur sont ceux qui illustrent une compétence, une discipline ou une qualité pertinente pour le poste. Exemples :
      </p>
      <ul>
        <li>Sport d&apos;équipe pratiqué régulièrement → esprit d&apos;équipe, persévérance</li>
        <li>Sport individuel de compétition → discipline personnelle, gestion du stress</li>
        <li>Engagement associatif actif (trésorier, président) → responsabilité, gestion</li>
        <li>Création de contenu (blog, chaîne YouTube, podcast) → compétences digitales, régularité</li>
        <li>Apprentissage d&apos;une langue ou d&apos;un instrument → curiosité, persévérance</li>
        <li>Voyages avec détails : "vécu 6 mois au Japon" → ouverture culturelle, adaptabilité</li>
      </ul>

      <h2>Ce qu&apos;il faut éviter</h2>
      <ul>
        <li>"Lecture, cinéma, voyages" — ces trois éléments sont dans 60 % des CVs. Ils ne disent rien.</li>
        <li>Les loisirs politiquement sensibles s&apos;ils ne sont pas directement pertinents pour le poste</li>
        <li>Les activités qui peuvent créer des préjugés selon le recruteur (certains sports extrêmes pour des postes à responsabilité peuvent soulever des questions sur la disponibilité)</li>
        <li>Une liste de 10 loisirs — cela ressemble à du remplissage</li>
      </ul>

      <h2>Comment formuler cette section</h2>
      <p>
        Ne listez pas seulement le nom de l&apos;activité — ajoutez une précision qui la rende concrète. Comparez :
      </p>
      <ul>
        <li>Avant : "Photographie"</li>
        <li>Après : "Photographie de rue — projet de documentation urbaine publié sur un blog depuis 3 ans (2 000 abonnés)"</li>
      </ul>
      <p>
        Ce niveau de détail transforme un loisir générique en signal de compétence et de régularité.
      </p>

      <h2>Quand supprimer complètement cette section</h2>
      <p>
        Si votre CV fait déjà 2 pages et que votre section loisirs n&apos;apporte rien de pertinent, supprimez-la sans hésiter. L&apos;espace récupéré est mieux utilisé pour détailler une expérience ou quantifier un résultat.
      </p>

      <blockquote>
        Un loisir sur un CV doit dire quelque chose sur vous en tant que professionnel. Si vous ne pouvez pas expliquer en quoi il illustre une qualité pertinente pour le poste, retirez-le.
      </blockquote>
    </div>
  )
}
