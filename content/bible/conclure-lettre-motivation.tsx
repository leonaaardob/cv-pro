export default function GuideConclureLettreMotivation() {
  return (
    <div className="prose-guide">
      <p>
        La conclusion d&apos;une lettre de motivation est aussi importante que son ouverture. C&apos;est la dernière chose que le recruteur lit avant de décider s&apos;il vous appelle. Une conclusion molle ou générrique efface une bonne première impression. Une conclusion forte peut rattraper une lettre moyenne.
      </p>

      <h2>Ce que la conclusion doit faire</h2>
      <p>
        Une bonne conclusion remplit trois fonctions en quelques lignes :
      </p>
      <ul>
        <li>Elle réaffirme votre intérêt pour le poste — sans répéter mot pour mot ce qui précède</li>
        <li>Elle formule une invitation à un entretien de façon directe et confiante</li>
        <li>Elle ferme la lettre avec une formule de politesse sobre et professionnelle</li>
      </ul>

      <h2>L&apos;appel à l&apos;action : direct et confiant</h2>
      <p>
        La formulation de l&apos;invitation à l&apos;entretien doit être active, pas passive. Comparez :
      </p>
      <ul>
        <li>Passif : "Dans l&apos;espoir de vous rencontrer prochainement, je reste à votre entière disposition."</li>
        <li>Actif : "Je serais heureux d&apos;échanger avec vous lors d&apos;un entretien. Je suis disponible à partir du [date] et joignable au [numéro]."</li>
      </ul>
      <p>
        La version active montre que vous savez prendre des initiatives. Elle donne également au recruteur les informations pratiques dont il a besoin sans qu&apos;il ait à chercher.
      </p>

      <h2>Ce qu&apos;il faut éviter dans la conclusion</h2>
      <ul>
        <li>"Dans l&apos;espoir que ma candidature retiendra votre attention" — place le recruteur en position de juge et le candidat en position de demandeur passif.</li>
        <li>"Restant dans l&apos;attente de votre réponse" — identique, trop passif.</li>
        <li>"Je vous remercie de l&apos;attention que vous porterez à ma candidature" — formule usée, sans valeur ajoutée.</li>
        <li>Répéter les éléments du CV ou du deuxième paragraphe — la conclusion n&apos;est pas un résumé.</li>
        <li>Ajouter de nouveaux arguments à la toute fin — si c&apos;est important, ça devait être dans le corps de la lettre.</li>
      </ul>

      <h2>Le ton de la conclusion</h2>
      <p>
        Le ton doit être sobre, confiant et cordial. Évitez la familiarité excessive ("avec enthousiasme !", "j&apos;ai hâte de vous rencontrer !") autant que la froideur administrative. Quelques exemples de formules de politesse adaptées à 2026 :
      </p>
      <ul>
        <li>"Cordialement" — valeur sûre, sobre, toujours approprié</li>
        <li>"Bien cordialement" — légèrement plus chaleureux, adapté aux contextes PME ou startup</li>
        <li>"Avec mes cordiales salutations" — plus formel, adapté aux candidatures dans de grandes structures ou la fonction publique</li>
      </ul>

      <blockquote>
        La conclusion est votre dernière impression. Elle ne doit pas être la plus longue — elle doit être la plus nette. Fermez la lettre comme vous fermeriez un entretien réussi : avec confiance et clarté.
      </blockquote>
    </div>
  )
}
